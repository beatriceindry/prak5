    <h2 style="text-align: center; color: maroon">B A R O B B O</h2>
    <p style="font-size: large">Barobbo adalah makanan khas selanjutnya, makanan ini terbuat dari butiran-butiran jagung yang dicampur dengan sayur-sayuran dan bahan lainnya, makanan ini adalah salah satu makanan faforite orang  bugis karena rasanya yang enak dan gimana gitu, bahan dasar makanan ini adalah jagung dan bisa juga ditambahkan udang atau irisan ayam, agar lebih enak, bumbunya adalah cukup dengan masako atau bisa dengan bumbu racikan.</p>
    <p style="font-size: large">Berikut adalah bahan-bahan dan cara pembuatannya: </p>
    <h3 style="color:green">Bahan: </h3>
    <ol>
      <li>70 gram beras</li>
      <li>150 gramudang segar (dikupas atau utuh tergantung selera)</li>
      <li>3 bonggol jagung manis ukuran besar</li>
      <li>1200 ml air atau secukupnya</li>
      <li>1 batang daun bawang potong potong</li>
      <li>Gula pasir, garam dan kaldu bubuk secukupnya</li>
    </ol>
    <h3 style="color:green">Untuk Tambahan Sayuran Lain Sesuai Selera: </h3>
    <ol>
      <li>Labu kuning</li>
      <li>Kacang panjang</li>
      <li>Kangkung</li>
      <li>Bayam</li>
    </ol>
    <h3 style="color:green">Bumbu Yang Dihaluskan: </h3>
    <ol>
      <li>6 siung bawang merah</li>
      <li>4 siung bawang putih</li>
      <li>1/2 sdt merica bubuk</li>
      <li>Lalu tumis dengan potongan daun bawang hingga harum dengan minyak goreng secukupnya.</li>
    </ol>
    <h3 style="color:green">Pelengkap: </h3>
    <ol>
      <li>Dadar jagung udang atau telur dadar, sambel, ikan asin, jeruk nipis, irisan seledri, bawang goreng.</li>
    </ol>
    <h3 style="color:green">Langkah: </h3>
    <ol>
      <li>Didihkan Air dalam panci, lalu masukan beras, masak hingga lunak.</li>
      <li>Sisir Jagung, potong potong labu kuning dan kacang panjang, siangi bayam dan kangkung,cuci tiriskan.</li>
      <li>Setelah beras mulai lunak, masukan udang segar, jagung dan labu aduk rata, didihkan kembali hingga semua matang.</li>
      <li>Masukan bumbu yang sudah ditumis, beri gula garam dan kaldu bubuk, cek rasa.</li>
      <li>Terakhir masukan potongan kacang panjang, kangkung dan bayam, aduk rata didihkan beberapa saat hingga layu, matikan api.</li>
      <li>Sajikan hangat Barobbo bersama pelengkapnya.</li>
    </ol>
