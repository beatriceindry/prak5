    <h2 style="text-align: center; color: skyblue">S O P  S A U D A R A</h2><hr>
    <p>Sop saudara merupakan kuliner khas Sulawesi Selatan yang sayang untuk dilewatkan. Sama seperti coto makassar yang berbahan utama daging sapi, sop saudara menambahkan soun dan perkedel dalam semangkuk sajiannya. Yuk, memasaknya di rumah dengan resep praktis dan otentik berikut.</p>
    <h3 style="color: skyblue">Bahan-bahan:</h3>
    <ol>
      <li>Daging sapi, potong dadu - 250 gram</li>
      <li>Kayu manis - 1 ruas jari</li>
      <li>Garam - 2 sdt</li>
      <li>Gula pasir - 1 sdt</li>
      <li>Kaldu bubuk - 1/2 sdt</li>
      <li>Air - 3 gelas</li>
      <li>Minyak goreng - 3 sdm</li>
    </ol>
    <h3 style="color: skyblue">Bumbu Halus:</h3>
    <ol>
      <li>Bawang putih - 4 siung</li>
      <li>Bawang merah - 6 buah</li>
      <li>Kemiri - 2 buah</li>
      <li>Kunyit - 1/2 ruas jari</li>
      <li>Pala - 1/4 butir</li>
      <li>Jinten - 1 sdt</li>
      <li>Ketumbar, sangrai - 1 sdt</li>
    </ol>
    <h3 style="color: skyblue">Bumbu Lainnya:</h3>
    <ol>
      <li>Serai, memarkan - 1 batang</li>
      <li>Jahe, memarkan - 1/2 buah</li>
      <li>Lengkuas, memarkan - 3/4 buah</li>
      <li>Daun jeruk iris kasar - 2 lembar</li>
    </ol>
    <h3 style="color: skyblue">Pelengkap:</h3>
    <ol>
      <li>Soun, seduh air panas lalu tiriskan - 50 gram</li>
      <li>Perkedel kentang - 3 buah</li>
      <li>Daun bawang, iris halus - 1 batang</li>
      <li>Bawang goreng - secukupnya</li>
    </ol>
    <h3 style="color: skyblue">Langkah:</h3>
    <ol>
      <li>Dalam panci, masukkan daging sapi dan air. Rebus daging hingga benar-benar empuk. Angkat dan sisihkan dagingnya, lalu iris-iris kecil. Sisihkan pula air kaldu bekas rebusan daging.</li>
      <li>Panaskan minyak. Tumis bumbu halus hingga harum.</li>
      <li>Masukkan bumbu lainnya kecuali kayu manis. Tumis kembali hingga layu. Matikan api.</li>
      <li>Dalam panci, didihkan kembali air kaldu rebusan daging.</li>
      <li>Masukkan bumbu tumis, kayu manis, garam, gula, kaldu bubuk, dan daging. Aduk rata dan masak hingga mendidih serta bumbunya meresap. Koreksi rasanya lalu matikan api.</li>
      <li>Tata soun, perkedel, daun bawang, dan bawang goreng dalam mangkuk. Siram dengan kuah sop.</li>
      <li>Siap Disajikan</li>
    </ol>
    <h3 style="color: skyblue">Tips:</h3>
    <ol>
      <li>Daging sapi bisa direbus dengan panci presto supaya cepat empuk dan keluar kaldunya.</li>
      <li>Selain perkedel kentang, sop saudara juga biasa disajikan bersama telur rebus. </li>
    </ol>
