    <h2 style="text-align: center; color: maroon">K A P U R U N G</h2>
    <p>Kapurung merupakan makanan yang mirip dengan papeda yakni terbuat dari bahan dasar sagu. Namun kapurung di Sulawesi memiliki rasa yang lebih menggoda karena dimasak dengan berbagai macam sayuran dan daging. Kuah dari kapurung juga terlihat seperti kuah gulai sehingga semakin memikat mata.</p>
    <p>Dari tampilannya, kapurung memang begitu mirip dengan soto namun jika dirasakan akan terasa sekali lembutnya papeda yang dikombinasi dengan segarnya kuah dari berbagai macam sayur dan daging yang diberi berbagai macam bumbu.</p>
    <p>Berikut ini bahan-bahan dan cara pembuatannya:</p>
    <h3 style="color: maroon">Bahan-bahan:</h3>
    <ol>
      <li>100 gram udang ukuran 13-16</li>
      <li>200 gram ikan kembung (atau ikan tongkol) atau ikan teri mirong, boleh ganti ikan</li>
      <li>200 gram tepung sagu ( sagu segar asli langsung ambil dari pohon sagu, jika tidak ada bisa gunakan tepung kanji atau sagu tani banyak tersedia di supermarket)</li>
      <li>8 mata asam patikala (buah kecombrang digeprek) bisa ganti air asam jawa dan ditambah potongan tangkai bunga kecombrang yang masih lunak</li>
    </ol>
    <h3 style="color: maroon">Bahan Sayuran:</h3>
    <ol>
      <li>Kangkung</li>
      <li>Kacang panjang</li>
      <li>Jagung manis muda</li>
      <li>Bayam</li>
      <li>Terong</li>
      <li>Jantung pisang dan tomat buah. Bersihkan, lalu potong-potong</li>
    </ol>
    <h3 style="color: maroon">Bumbu Halus:</h3>
    <ol>
      <li>4 sdm kacang tanah goreng</li>
      <li>1/2 sdt terasi</li>
      <li>3 buah cabe merah</li>
      <li>1 sdm garam</li>
      <li>Penyedap rasa</li>
      <li>2 butir bawang merah</li>
      <li>1 buah tomat merah</li>
    </ol>
    <h3 style="color: maroon">Bahan Sambal:</h3>
    <ol>
      <li>1 buah tomat</li>
      <li>10 cabe rawit merah</li>
      <li>1/4 sdt garam</li>
      <li>1/2 sdt gula pasir putih</li>
      <li>1 buah jeruk limau</li>
    </ol>
    <h3 style="color: maroon">Langkah Membuat Kapurung:</h3>
    <ol>
      <li>Rebus ikan sampai hancur dalam 1,5 liter air. Selain itu, daging ikan juga bisa diblender dan diaduk rata menjadi kaldu ikan yang tebal.</li>
      <li>Masukkan semua bumbu ke dalam kaldu ikan.</li>
      <li>Masukkan sayur-mayur dan udang ke dalam kuah.</li>
      <li>Tepung sagu dicampur dengan sedikit air, lalu disiram air panas sampai warnanya berubah menjadi abu-abu. Bentuk sagu menjadi bola-bola sebesar bakso. Bola-bola sagu ini nantinya dimasukkan ke dalam kuah Kapurung</li>
      <li>Hidangkan Kapurung dalam kondisi masih panas. Bila dingin tampilan dan rasanya akan berubah.</li>
    </ol>
    <h3 style="color: maroon">Langkah Membuat Sambal:</h3>
    <ol>
      <li>Semua bahan ditumbuk atau diulek hingga halus.</li>
      <li>Jeruk limau dipotong menjadi dua dan diperaskan airnya kedalam sambal pedas.</li>
      <li>Sambal Kapurung siap disajikan bersama sayur kapurung.</li>
    </ol>
