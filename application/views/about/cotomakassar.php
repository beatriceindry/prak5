    <h2 style="text-align:center; color: #C71585">C O T O  M A K A S S A R</h2>
    <p>Salah Satu yang menjadi makanan khas Sulawesi Selatan yang terkenal enak adalah coto Makassar. Kota Makassar adalah salah satu kota yang sangat terkenal di Sulawesi Selatan. Para wisatawan yang berkunjung ke Makassar sebaiknya mencicipi makanan yang satu ini karena memiliki cita rasa yang sangat khas.</p>
    <p>Bahan yang digunakan untuk membuat coto Makassar adalah dari daging sapi yang di masak dengan air cucian beras. Rasa dari coto ini sangat istimewa karena banyak menggunakan bumbu yang lengkap. Sebenarnya coto Makassar sudah banyak dijual di banyak wilayah di Indonesia hanya saja jika anda mencoba langsung di Makassar tentu cita rasanya akan lebih otentik.</p>
    <p>Berikut ini bahan-bahan dan cara pembuatannya:</p>
    <h3 style="color:#C71585">Bahan-bahan:</h3>
    <ol>
      <li>750gr daging sandung lamur(atau pakai daging untung rendang)</li>
      <li>700ml air</li>
      <li>2 lembar daun salam</li>
    </ol>
    <h3 style="color:#C71585">Bahan A(Sangrai & Uleg)</h3>
    <ol>
      <li>3 sdm ketumbar butir</li>
      <li>1 1/2 sdm jintam utuh</li>
      <li>1/2 sdt merica utuh, sangrai</li>
    </ol>
    <h3 style="color:#C71585">Bahan B (Goreng)</h3>
    <ol>
      <li>15 siung bawang putih</li>
      <li>7 butir kemiri</li>
      <li>1 ruas lengkuas, iris-iris</li>
      <li>2 batang sereh, potong-potong</li>
    </ol>
    <h3 style="color:#C71585">Bahan C :</h3>
    <ol>
      <li>120gr kacang tanah (boleh ditambah supaya kuahnya lebih kental)</li>
    </ol>
    <h3 style="color:#C71585">Bahan Pelengkap:</h3>
    <ol>
      <li>1500ml air (500 ml dari air kaldu rebusan daging)</li>
      <li>5cm lengkuas, geprek</li>
      <li>4 lembar daun salam</li>
      <li>1 ruas jari jahe, geprek</li>
      <li>1 sdm garam</li>
      <li>1 sdm kaldu bubuk</li>
      <li>1/2 sdm gula pasir</li>
      <li>2 sdm minyak goreng untuk menumis bumbu</li>
    </ol>
    <h3 style="color:#C71585">Topping:</h3>
    <ol>
      <li>Bawang goreng</li>
      <li>Irisan daun bawang</li>
      <li>Jeruk nipis</li>
    </ol>
    <h3 style="color:#C71585">Langkah:</h3>
    <ol>
      <li>Potong potong daging, rebus dengan 4 lembar daun salam. Setelah empuk, potong kotak kotak sesuai selera. Saya pisah rebusan daging dan paru.</li>
      <li>Siapkan bumbu A. Siapkan bumbu B.</li>
      <li>Blender bumbu A dan B. Kemudian tumis hingga matang dan harum.</li>
      <li>Goreng kacang tanah, buang kulitnya, blender, sisihkan</li>
      <li>Rebus daging dengan 1500 ml air, 4 daun salam, campuran bumbu A dan B, lengkuas geprek, dan jahe geprek. Rebus selama 10 menit</li>
      <li>Masukkan kacang tanah yg telah di blender, gula, garam, dan penyedap masak lagi 10 menit, koreksi rasa</li>
      <li>Sajikan bersama bawang goreng dan daun bawang.</li>
    </ol>
